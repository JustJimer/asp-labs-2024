namespace LR3_Chupyna_ASP_402
{
    public class CalcService
    {
        public int Add(int a, int b)
        {
            return a + b;
        }

        public int Subtract(int a, int b)
        {
            return a - b;
        }
        public int Product(int a, int b)
        {
            return a * b;
        }

        public int Square(int a)
        {
            return a * a;
        }
    }
}
